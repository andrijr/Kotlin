fun main(args: Array<String>) {

    var x: Boolean
    x = 5 > 5 || 1 != 2 // or szuka do pierwszej prawdy i zwraca wynik prawda lub jak wszyskie falsz to zwraca falsz
    println(x)
    println("| - lub - OR")
    println(false || false) // || - lub - OR
    println(true || false) // || - lub - OR
    println(false || true) // || - lub - OR
    println(true || true) // || - lub - OR
    println("&& - AND - I")
    println(false && false) // && - AND - I
    println(true && false) // && - AND - I
    println(false && true) // && - AND - I
    println(true && true) // && - AND - I
    println("NOT - NIE")
    println(!true)
    println(!false)

    println(2 + 2 * 2)

    // negacja różności - !=

}
